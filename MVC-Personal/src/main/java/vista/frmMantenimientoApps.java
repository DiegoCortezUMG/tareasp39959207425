    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int registrosBorrados=0;
        clsApp app = new clsApp();
        app.setIdApp(Integer.parseInt(txtbuscado.getText()));
        registrosBorrados=app.setBorrarApp(app);
        JOptionPane.showMessageDialog(null, "Registro Borrado\n",
            "Información del Sistema", JOptionPane.INFORMATION_MESSAGE);
        llenadoDeTablas();
        limpiarTextos();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        clsApp app = new clsApp();
        app.setNombreApp(txtNombre.getText());
        app.setEstadoApp(txtContrasena.getText());
        app.setIngresarApp(app);
        JOptionPane.showMessageDialog(null, "Registro Ingresado\n",
            "Información del Sistema", JOptionPane.INFORMATION_MESSAGE);
        llenadoDeTablas();
        limpiarTextos();
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        clsApp app = new clsApp();
        //usuario.setNombreUsuario(txtbuscado.getText());
        app.setIdApp(Integer.parseInt(txtbuscado.getText()));
        app = app.getBuscarInformacionAppPorId(app);
        System.out.println("Usuario retornado:" + app);
        txtNombre.setText(app.getNombreApp());
        txtContrasena.setText(app.getEstadoApp());
    }//GEN-LAST:event_btnBuscarActionPerformed


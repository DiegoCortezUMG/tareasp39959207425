public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("nombre");
        modelo.addColumn("estado");
        clsApp usuario = new clsApp();
        //VendedorDAO vendedorDAO = new VendedorDAO();
        List<clsApp> listaApps = usuario.getListadoApps();
        TablaApps.setModel(modelo);
        String[] dato = new String[3];
        for (int i = 0; i < listaApps.size(); i++) {
            dato[0] = Integer.toString(listaApps.get(i).getIdApp());
            dato[1] = listaApps.get(i).getNombreApp();
            dato[2] = listaApps.get(i).getEstadoApp();
            modelo.addRow(dato);
        }       
    }
